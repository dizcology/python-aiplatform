def make_parent(parent: str) -> str:
    parent = parent

    return parent

def make_metadata_schema(schema: str) -> google.cloud.aiplatform_v1beta1.types.metadata_schema.MetadataSchema:
    metadata_schema = {'schema': schema}

    return metadata_schema

def make_metadata_schema_id(metadata_schema_id: str) -> str:
    metadata_schema_id = metadata_schema_id

    return metadata_schema_id

